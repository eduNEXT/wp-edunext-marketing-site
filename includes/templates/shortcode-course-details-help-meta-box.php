<p>Example of usage:
<pre>
[course_details
    course_id="edX+DemoX"
    course_details="title, short_description, start, video"
    course_title_styles="my-custom-title-style-1, my-custom-title-style-2"
    course_video_styles="my-custom-video-style"]
</pre>
<p>Shortcode attributes:</p>
<ul>
    <li><strong>course_id - Required</strong></li><p>Open edX Discovery API course id, it must include the short organization name and course number. e.g edX+DemoX</p>
    <li><strong>course_details - Required</strong></li><p>String or list separated by commas, with desired course details. (title, short_description, start, video)</p>
    <li><strong>course_title_styles - Optional</strong></li><p>String or list separated by commas, with desired class names. e.g my-awesome-style</p>
    <li><strong>course_short_description_styles - Optional</strong></li><p>String or list separated by commas, with desired class names. e.g my-awesome-style</p>
    <li><strong>course_start_styles - Optional</strong></li><p>String or list separated by commas, with desired class names. e.g my-awesome-style</p>
    <li><strong>course_video_styles - Optional</strong></li><p>String or list separated by commas, with desired class names. e.g my-awesome-style</p>
</ul>
